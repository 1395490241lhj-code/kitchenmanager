// 保护词：带「汤/粉」字的真实食物名，绝不按烹饪介质处理。
const NON_STOCK_PROTECT_REGEX = /(汤圆|汤面|汤粉|米粉|河粉|凉粉|粉丝|粉条)/;
const NON_STOCK_EXACT = new Set([
  '水', '汤', '汁',
  '清水', '开水', '沸水', '热水', '温水', '冷水', '凉水', '冰水',
  '高汤', '清汤', '鲜汤', '肉汤', '鸡汤', '骨汤', '原汤', '汤汁', '汤底', '锅底', '汤料',
  '适量', '少许', '若干', '些许', '适当', '备用', '按需', '适口', '少量', '适量即可'
]);
export function isNonStockCookingTerm(name) {
  const n = String(name || '').trim();
  if (!n) return true;
  if (NON_STOCK_PROTECT_REGEX.test(n)) return false;
  if (NON_STOCK_EXACT.has(n)) return true;
  // 「老母鸡汤 / 牛骨汤 / 猪骨高汤」等以汤类词结尾的变体（最多 4 个前缀字）。
  if (/^[一-龥]{0,4}(高汤|清汤|鲜汤|肉汤|鸡汤|骨汤|原汤|汤汁)$/.test(n)) return true;
  return false;
}

