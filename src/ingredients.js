export function isSeasoning(name) {
  if (!name) return true;
  const n = String(name).trim();
  if (SEASONINGS.has(n)) return true;
  if (n.length <= 3 && (n.includes("盐") || n.includes("糖") || n.includes("醋") || n.includes("酱") || n.includes("油"))) return true;
  return false;
}

export function explodeCombinedItems(list) {
  const out = [];
  for(const it of (list||[])){
    const name = String(it.item||'').trim();
    if(!name) continue;
    const hasQty = typeof it.qty === 'number' && isFinite(it.qty);
    if(SEP_RE.test(name) && !hasQty){
      for(const n of name.split(SEP_RE).map(s=>s.trim()).filter(Boolean)){ out.push({ item:n, qty:null, unit:null }); }
    }else{ out.push(it); }
  }
  return out;
}

export function guessShelfDays(name, unit) {
  if(isDryGoodName(name)) return 365;
  const veg=['菜','叶','苔','苗','芹','香菜','葱','椒','瓜','番茄','西红柿','豆角','笋','蘑','菇','花菜','西兰花','菜花','茄子','豆腐','生菜','莴','空心菜','韭','蒜苗','青椒','黄瓜'];
  if(veg.some(w=>String(name || '').includes(w)))return 5;
  if(unit==='ml')return 30;
  if(unit==='pcs')return 14;
  return 7;
}

export function guessKitchenUnit(name) {
  const n = getCanonicalName(name || '');
  const includesAny = words => words.some(w => n.includes(w));
  if (includesAny(['鸡蛋', '鸭蛋', '番茄', '西红柿', '土豆', '洋葱', '青椒', '茄子', '苹果', '梨'])) return '个';
  if (includesAny(['豆腐', '酸奶', '盒装', '奶油', '蘑菇', '菌菇'])) return '盒';
  if (includesAny(['米', '大米', '面粉', '挂面', '面条', '粉丝', '速冻', '饺子', '馄饨'])) return '袋';
  if (includesAny(['酱油', '生抽', '老抽', '醋', '料酒', '油', '牛奶', '饮料'])) return '瓶';
  if (isDryGoodName(n)) return getDryGoodConfig(n)?.unit || '包';
  if (includesAny(['葱', '香菜', '芹菜', '韭菜', '蒜苗', '菠菜', '青菜', '木耳菜', '空心菜', '苋菜', '茼蒿', '芥兰', '豌豆尖', '油麦菜', 'A菜'])) return '把';
  if (includesAny(['猪肉', '牛肉', '羊肉', '鸡肉', '鸭肉', '排骨', '鱼', '虾', '肉'])) return '份';
  return '份';
}

export function normalizeKitchenAmount(name, qty, unit, options = {}) {
  const n = options.source === 'receipt'
    ? normalizeReceiptIngredientName(name)
    : getCanonicalName(name || '');
  let q = Number(qty) || 1;
  let u = String(unit || '').trim();
  const lowerUnit = u.toLowerCase();
  if (['pcs', 'piece', 'pieces'].includes(lowerUnit)) u = '个';
  else if (['box', 'boxes'].includes(lowerUnit)) u = '盒';
  else if (['bag', 'bags'].includes(lowerUnit)) u = '袋';
  else if (['pack', 'packs', 'package', 'packages', 'pkt'].includes(lowerUnit)) u = '包';
  else if (['bottle', 'bottles'].includes(lowerUnit)) u = '瓶';
  else if (['bunch', 'bunches'].includes(lowerUnit)) u = '把';

  if (['kg', '千克', '公斤'].includes(u)) { q *= 1000; u = 'g'; }
  if (['l', 'L', '升'].includes(u)) { q *= 1000; u = 'ml'; }
  if (!u) u = guessKitchenUnit(n);
  return { name: n, qty: Math.round(q * 100) / 100, unit: u };
}

import { perfMeasure as __perfMeasure } from './utils/perf.js?v=219';
export function buildCatalog(pack) {
  return __perfMeasure('buildCatalog', () => buildCatalogImpl(pack));
}
function buildCatalogImpl(pack) {
  const units = {}, set = new Set();
  for(const list of Object.values(pack.recipe_ingredients||{})){
    for(const it of explodeCombinedItems(list)){
      const n=(it.item||'').trim();
      if(!n) continue;
      // 只收核心食材进候选：调料（含姜片/蒜末/油类/淀粉勾芡等扩展写法）与
      // 水/汤/量词等非库存项不进食材页 datalist 与库存候选。
      if(isRecipeNonCoreName(n)) continue;
      units[n]=units[n]||it.unit||'g';
      set.add(n);
    }
  }
  return Array.from(set).sort().map(n=>({name:n, unit:units[n]||'g', shelf:guessShelfDays(n, units[n]||'g')}));
}

/* ──────────────────────────────────────────────────────────────────────────
 * 计件 / 档位 双轨食材模型（油表模型）
 *  - PIECE（计件）：离散可数（个、颗、根、片…），按整数加减。
 *  - GEAR（档位）：散装蔬菜 / 调料 / 散装肉，按 [100,75,50,25,0] 档位降档。
 * ──────────────────────────────────────────────────────────────────────── */
export const UNIT_TYPE = { PIECE: 'PIECE', GEAR: 'GEAR' };

// 离散可数单位 → 计件
const PIECE_UNITS = new Set(['个', '颗', '只', '根', '片', '块', '枚', '瓣', '条', '棵', '头', '张', '支', '盒', '袋', '包', '瓶', '罐', '听', '份']);

/**
 * 判定食材属于「计件(PIECE)」还是「档位(GEAR)」。
 * 优先看单位；调味料一律按档位（油表模型）；无单位时用 guessKitchenUnit 猜测。
 */
export function getUnitType(name, unit) {
  const canonical = getCanonicalName(name || '');
  if (isSeasoning(canonical)) return UNIT_TYPE.GEAR;
  const u = (unit || '').trim();
  if (u) return PIECE_UNITS.has(u) ? UNIT_TYPE.PIECE : UNIT_TYPE.GEAR;
  const guessed = guessKitchenUnit(canonical) || '';
  return PIECE_UNITS.has(guessed) ? UNIT_TYPE.PIECE : UNIT_TYPE.GEAR;
}
