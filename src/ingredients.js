export function normalizeReceiptIngredientName(name) {
  if (!name) return "";
  let n = String(name).toLowerCase().trim();
  n = n.replace(/（.*?）|\(.*?\)/g, ' ').trim();
  n = n.replace(/-/g, ' ');
  n = n.replace(/\s+/g, ' ').trim();

  const stopWords = ["organic", "fresh", "raw", "wild", "premium", "choice", "natural"];
  let words = n.split(' ');
  words = words.filter(w => !stopWords.includes(w));
  n = words.join(' ');

  if (ENGLISH_INGREDIENT_ALIASES[n]) {
    return getCanonicalName(ENGLISH_INGREDIENT_ALIASES[n]);
  }

  let singular = n;
  if (n.endsWith('s')) {
    if (n.endsWith('es')) {
      singular = n.slice(0, -2);
      if (ENGLISH_INGREDIENT_ALIASES[singular]) {
        return getCanonicalName(ENGLISH_INGREDIENT_ALIASES[singular]);
      }
    }
    singular = n.slice(0, -1);
    if (ENGLISH_INGREDIENT_ALIASES[singular]) {
      return getCanonicalName(ENGLISH_INGREDIENT_ALIASES[singular]);
    }
  }

  for (const [eng, cn] of Object.entries(ENGLISH_INGREDIENT_ALIASES)) {
    if (n === eng || n.includes(eng)) {
      return getCanonicalName(cn);
    }
    if (eng.endsWith('s')) {
      const singEng = eng.slice(0, -1);
      if (n.includes(singEng)) {
        return getCanonicalName(cn);
      }
    }
  }

  return getCanonicalName(name);
}
export const SEP_RE = /[，,、/;；|]+/;

export function checkAlias(name) {
  if (INGREDIENT_ALIASES[name]) return name;
  for (const [canonical, aliases] of Object.entries(INGREDIENT_ALIASES)) {
    if (aliases.includes(name)) return canonical;
  }
  return null;
}

// ── 热路径 memo（性能）────────────────────────────────────────────────────
// getCanonicalName / family / matchNames / 非核心判定 全部由静态表驱动（别名表、
// 食材族、正则常量），同输入恒同输出 → 模块级 Map 缓存安全、永不失效。
// 推荐计算每次首页渲染会调用它们数十万次，无缓存时满库渲染可达 20s+。
const canonicalNameMemo = new Map();
const familyKeyMemo = new Map();
const matchNamesMemo = new Map();
const nonCoreNameMemo = new Map();

export function getCanonicalName(name) {
  if(!name) return "";
  const memoKey = String(name);
  const hit = canonicalNameMemo.get(memoKey);
  if (hit !== undefined) return hit;
  const result = computeCanonicalName(memoKey);
  canonicalNameMemo.set(memoKey, result);
  return result;
}

function computeCanonicalName(name) {
  let n = String(name).trim();
  if (checkAlias(n)) return checkAlias(n);
  const noParens = n.replace(/（.*?）|\(.*?\)/g, '').trim();
  if (noParens !== n && checkAlias(noParens)) return checkAlias(noParens);
  const prefixes = ["熟", "生", "鲜", "干", "水发", "净", "嫩"];
  let cleanPrefix = n;
  for (const p of prefixes) {
    if (cleanPrefix.startsWith(p)) cleanPrefix = cleanPrefix.substring(p.length).trim();
  }
  if (checkAlias(cleanPrefix)) return checkAlias(cleanPrefix);
  const suffixes = ["肉", "片", "丝", "末", "丁", "块", "条", "泥", "茸", "尖", "头", "仁", "皮", "腿"];
  let cleanSuffix = cleanPrefix;
  for (const s of suffixes) {
    if (cleanSuffix.endsWith(s)) {
      const tryName = cleanSuffix.slice(0, -s.length);
      if (checkAlias(tryName)) return checkAlias(tryName);
    }
  }
  return n;
}

